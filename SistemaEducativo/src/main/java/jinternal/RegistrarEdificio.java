    private void btnRegistrarEdificioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarEdificioActionPerformed
        // TODO add your handling code here:
        
        
        String nombreEdificio = txtNombreEdificio.getText();
        
        int longitudNombreEdificio = nombreEdificio.length();
        
                
        if (longitudNombreEdificio<=0){
            
            JOptionPane.showMessageDialog(null, "Error, debes ingresar un nombre de edificio","Mensaje",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        int confirmar = JOptionPane.showConfirmDialog(null,"Â¿Deseas guardar los datos?","Mensaje",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        
        if (confirmar==JOptionPane.YES_OPTION){
            
            String consulta = "CALL sp_agregarEdificio(?)";
            
            Object[] params = {nombreEdificio};
            
            ResultSet res = Conexion.getInstancia().hacerConsulta(consulta, params);
            
            try{
                if (res!=null){
                    
                    if(res.next()){
                        String mensajeObtenido = res.getString("mensaje");
                        
                        if (mensajeObtenido.equals("yaexiste")){
                            JOptionPane.showMessageDialog(null, "Error, edificio ya existe.","Mensaje",JOptionPane.ERROR_MESSAGE);
                        }else if (mensajeObtenido.equals("registrado")){
                            JOptionPane.showMessageDialog(null, "El edificio fue registrado.","Mensaje",JOptionPane.INFORMATION_MESSAGE);
                            
                            txtNombreEdificio.setText("");
                            
                            ListadoEdificios.getInstancia().getModelo().actualizarJTable();
                            
                        }else if (mensajeObtenido.equals("errorproducido")){
                            JOptionPane.showMessageDialog(null, "Error producido.","Mensaje",JOptionPane.ERROR_MESSAGE);
  
                        }else{
                                
                        }
                    }
                }
            }catch(Exception ex){
                ex.printStackTrace();
            }
        }
        
        
    }//GEN-LAST:event_btnRegistrarEdificioActionPerformed


